var Grade = 'v';

switch(Grade){
    case 'a':
        console.log("Excellent");
        break;

    case 'b':
        console.log("Good");
        break;

    case 'c':
        console.log("Normal");
        break;

    case 'd':
        console.log("Bad");
        break;

    default:
        console.log("Error");
}